function add(a,b){
    return a+b;
}
function sub(a,b){
    return a-b;
}
function mul(a,b){
    return a*b;
}
function div(a,b){
    return a/b;
}
function mod(a,b){
    return a%b;
}
console.log(add(10,20));
console.log(sub(10,20));
console.log(mul(10,20));
console.log(div(10,20));
console.log(mod(10,20));

